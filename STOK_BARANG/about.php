        Inventory dalam bahasa Indonesia berarti persediaan. Sedangkan sistem inventory memiliki makna pengaturan persediaan dan berkaitan dengan aktivitas logistik sebuah perusahaan. Di mana kegiatan dari sistem tersebut termasuk dalam pengecekan dan penyediaan stok bahan baku atau barang setengah jadi ataupun barang jadi, demi kelancaran proses produksi atau pemenuhan permintaan pelanggan.
