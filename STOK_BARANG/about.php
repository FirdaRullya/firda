      <div class="card card-body" align="justify">
        Sistem inventory berguna untuk menentukan jumlah persediaan yang optimal dengan biaya total yang minimal. Persediaan atau inventory meliputi bahan mentah atau bahan baku, bahan pembantu, bahan dalam proses atau work in process, suku cadang, dan barang jadi atau finished good, karena timbulnya ketidakpastian permintaan, ketidakpastian pasokan supplier, dan ketidakpastian waktu pemesanan. Dalam menejemen persediaan adanya sistem inventory ini memiliki beberapa manfaat antara lain :
<br/>
<br/>
<b>a. Menghindari Kekurangan Bahan (Out Of Stock)</b>
Jika pelanggan datang untuk membeli barang dagangan, kemudian perusahaan tidak mempunyai barang tersebut, karena tidak adanya sistem inventory yang baik, maka perusahaan akan kehilangan kesempatan untuk memperoleh keuntungan. Untuk menghindari situasi tersebut, perusahaan harus mempunyai persedian barang yang terkontrol. 
<br/>
<br/>
<b>b. Meningkatkan Pemasaran</b>
Jika perusahaan mempunyai persediaan  barang dagangan yang lengkap, maka pelanggan/calon pelanggan akan terkesan dengan kelengkapan barang dagangan yang Anda tawarkan. Reputasi perusahaan bisa meningkat. Di samping itu, jika perusahaan selalu mampu memenuhi keinginan pelanggan pada saat dibutuhkan maka kepuasan pelanggan semakin baik, dan perusahaan semakin untung.
<br/>
<br/>
<b>c. Peningkatan Pelayanan</b>
Pelanggan tidak hanya meminta kecepatan pengantaran tetapi juga ketepatan dan kepercayaan. Adanya Pengintegrasian stok barang dengan penjualan melalui sistem inventory akan memungkinkan otomatisasi untuk memenuhi permintaan dengan mudah. Hal ini menjamin bahwa produk yang benar berada di tempat yang benar pada waktu yang tepat. Perusahaan akan dapat merespon permintaan pelanggan secara cepat karena otomatis akan selalu terhubung dengan adanya ketersediaan barang di gudang maupun di toko.
<br/>
<br/>
<b>d. Pengontrolan Persediaan & Pengambilan Keputusan</b>
Sistem inventory memungkinkan adanya fleksibilitas dari distribusi dan penyimpanan barang-barang secara menyeluruh sehingga memungkinkan perusahaan untuk memantau dan mengontrol persediaan sesuai dengan bisnis mereka. Adanya akses instan terhadap data-data penting meliputi ketersediaan persediaan, jumlah yang ada, jumlah yang harus dipesan lagi, dan biaya yang dapat diketahui pada saat itu juga terhadap persediaan, akan mempermudah perusahaan mengambil keputusan terkait persediaan barang yang harus mereka miliki dan besarnya biaya yang harus di bayarkan.
